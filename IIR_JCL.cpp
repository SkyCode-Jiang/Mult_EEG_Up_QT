/*********************************************************************************************************
 * 函数名称：StartIIR_LP
 * 函数功能：接收数据并输出 IIR 滤波后数据
 * 输入参数：input-输入的数据
 * 输出参数：
 * 返 回 值：
 * 创建日期：
 * 注    意：
 *********************************************************************************************************/
double StartIIR_LP(double input)
{
    uint8_t i;
    static double w_LP[IIR_ORDER+1];
    double output;

    w_LP[0] = A_LP[0] * input;
    for (i = 1; i <= IIR_ORDER; i++)
    {
        w_LP[0] -= A_LP[i] * w_LP[i];
    }

    for (i = 0; i <= IIR_ORDER; i++)
    {
        output += B_LP[i] * w_LP[i];
    }

    for (i = IIR_ORDER; i > 0; i--)
    {
        w_LP[i] = w_LP[i-1];
    }

    return output;
}

